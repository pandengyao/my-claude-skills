#!/bin/bash
# 控制台输出模板
# 用途：在交互和非交互模式下输出评审摘要
# 格式：纯文本，使用 heredoc 输出
# 
# 占位符说明：
# {{SUMMARY}}          - 评审总结一句话
# {{DECISION}}         - 评审结论（✅ 批准合并 / 💬 仅评论 / 🔄 请求修改后重新评审）
# {{P0_COUNT}}         - P0 严重问题数量
# {{P0_ISSUES}}        - P0 问题列表（如果无问题，填充："无"）
# {{P1_COUNT}}         - P1 主要问题数量
# {{P1_ISSUES}}        - P1 问题列表（如果无问题，填充："无"）
# {{P2_COUNT}}         - P2 小问题数量
# {{P2_ISSUES}}        - P2 问题列表（如果无问题，填充："无"）
# {{P3_COUNT}}         - P3 建议数量
# {{P3_ISSUES}}        - P3 建议列表（如果无问题，填充："无"）
#
# 问题格式示例（每个问题）：
# 1. **[问题标题]**
#    - 位置：[文件路径:行号]
#    - 问题：[问题描述]
#    - 建议：[修复建议]
#
# （问题之间用一个空行分隔）

cat <<'EOF'
评审已完成。{{SUMMARY}}

## 评审结论
{{DECISION}}

## 问题列表

### 🔴 P0 严重问题（{{P0_COUNT}}个）

{{P0_ISSUES}}

### 🟡 P1 主要问题（{{P1_COUNT}}个）

{{P1_ISSUES}}

### 🟢 P2 小问题（{{P2_COUNT}}个）

{{P2_ISSUES}}

### 💡 P3 建议（{{P3_COUNT}}个）

{{P3_ISSUES}}

---
📄 完整评审报告已生成: code-review-result.html
EOF
